    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        String check = "SELECT * FROM parts_stock WHERE part_number='"+number.getText()+"'";
        try {
            pst = conn.prepareStatement(check);
            rs = pst.executeQuery();
            
            if((!rs.next())){
                    
                    if (!number.getText().isEmpty() && !name.getText().isEmpty() && !quantity.getText().isEmpty()){
                    String query = "INSERT INTO parts_stock (part_number,part_name,type,total_qty) value(?,?,?,?)";
                    pst = conn.prepareStatement(query);
               
                    pst.setString(1, number.getText());
                    pst.setString(2, name.getText());
               
                    String value=type.getSelectedItem().toString();
                    pst.setString(3, value);
                    
                    pst.setString(4, quantity.getText());
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Data Berhasil  Ditambah");
                    show_data();
                    clear();
                    }
                    
                }else{
                JOptionPane.showMessageDialog(null, "Data Sudah ada");   
                clear();
                }
        } catch (SQLException ex) {
            Logger.getLogger(parts.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }//GEN-LAST:event_btnaddActionPerformed

