    private void show_data(){
        String perintah = "SELECT * FROM  parts_stock";
        try {
            pst = conn.prepareStatement(perintah);
            rs = pst.executeQuery();
            
            DefaultTableModel makan = new DefaultTableModel();
            makan.addColumn("Part Number");
            makan.addColumn("Part name");
            makan.addColumn("Type");
            makan.addColumn("Quantity");
        while (rs.next()){
            makan.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
        }
        dataparts.setModel(makan);
        } catch (SQLException ex) {
            
        }
    }
    
    
