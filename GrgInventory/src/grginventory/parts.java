    private void datapartsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_datapartsMouseClicked
        // TODO add your handling code here:
        int row = dataparts.getSelectedRow();
        String diklik = (dataparts.getModel().getValueAt(row, 0).toString());
        String sql = "SELECT * FROM parts_stock WHERE part_number='"+diklik+"'";
        
            try {
                pst = conn.prepareStatement(sql);
                rs = pst.executeQuery();
                
                 if((rs.next())){
                     String tambah1 = rs.getString("part_number");
                     number.setText(tambah1);
                     String tambah2 = rs.getString("part_name");
                     name.setText(tambah2);
                     String tambah3 = rs.getString("type");
                     type.setSelectedItem(tambah3);
                     String tambah4 = rs.getString("total_qty");
                     quantity.setText(tambah4);
                 }
            } catch (SQLException ex) {
                Logger.getLogger(parts.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_datapartsMouseClicked

