    private void partsinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_partsinMouseClicked
        // TODO add your handling code here:
        int row = partsin.getSelectedRow();
        String diklik = (partsin.getModel().getValueAt(row, 0).toString());
        String sql = "SELECT * FROM parts_in WHERE kd_in='"+diklik+"'";

        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            if((rs.next())){
                
                String tambah2 = rs.getString("kd_in");
                kdin.setText(tambah2);
                String tambah1 = rs.getString("qty");
                qty.setText(tambah1);
                String tambah11 = rs.getString("date_in");
                ((JTextField)datein.getDateEditor().getUiComponent()).setText(tambah11);
                
                String item2 = rs.getString("status");
                cbstatus.setSelectedItem(item2);
                String item = rs.getString("part_number");
                cbnumber.setSelectedItem(item);
            }
            /////////////////////////////////////cek stock//////////////////////
            String diklik2 = (partsin.getModel().getValueAt(row, 3).toString());
            String sql2 = "SELECT * FROM parts_stock WHERE part_number='"+diklik2+"'";
            pst = conn.prepareStatement(sql2);
            rs = pst.executeQuery();
            if((rs.next())){
                String tambah2 = rs.getString("total_qty");
                firstqty.setText(tambah2);    
            }
            ////////////////////////////////////////////////////////////////////
            String diklik3 = (partsin.getModel().getValueAt(row, 5).toString());
            String sql3 = "SELECT * FROM partner WHERE kd_partner='"+diklik3+"'";
            pst = conn.prepareStatement(sql3);
            rs = pst.executeQuery();
            if((rs.next())){
                String item1 = rs.getString("kd_partner");
                cbpartner.setSelectedItem(item1); 
            }
            ////////////////////////////////////////////////////////////////////
        } catch (SQLException ex) {
            Logger.getLogger(parts_io.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_partsinMouseClicked

