    private void show_data(){
        String perintah = "SELECT * FROM  parts_in";
        try {
            pst = conn.prepareStatement(perintah);
            rs = pst.executeQuery();
            
            DefaultTableModel makan = new DefaultTableModel();
            makan.addColumn("In Codes");
            makan.addColumn("Date In");
            makan.addColumn("Quantity");
            makan.addColumn("Part Number");
            makan.addColumn("Partner Code");
            makan.addColumn("Status");
        while (rs.next()){
            makan.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
        }
        partsin.setModel(makan);
        } catch (SQLException ex) {
            
        }
    }
    
